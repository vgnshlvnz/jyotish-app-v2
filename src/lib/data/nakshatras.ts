/**
 * The 27 nakshatras (lunar mansions) of Vedic astrology.
 *
 * Each entry covers a 13°20' span of the zodiac, four padas (3°20' each),
 * Vimshottari dasha lord, deity, shakti, classification keys, and related lore.
 *
 * Classical-source variation choices documented inline:
 *
 * - **Varna**: assigned per the Vimshottari-lord-derived Vedic tradition.
 *   Sources differ; alternative schemes are common.
 * - **Purushartha**: cycle per Hart de Fouw / Robert Svoboda
 *   (dharma → artha → kama → moksha across each 9-nakshatra cycle, with
 *   the 9th repeating moksha). Classical sources vary.
 * - **Mahabhuta**: derived from the Vimshottari dasha lord's element
 *   (Ketu/Sun/Mars = tejas; Venus/Moon = apas; Rahu/Saturn = vayu;
 *   Jupiter = akasha; Mercury = prithvi). Direct nakshatra-mahabhuta
 *   mapping is not given by BPHS; this is a defensible derivation.
 * - **Guna**: derived from Vimshottari lord (Sun/Moon/Jupiter = sattva;
 *   Mercury/Venus = rajas; Mars/Saturn/Rahu/Ketu = tamas). Sources vary.
 * - **Direction**: follows the dominant rashi (the rashi where the
 *   nakshatra spends more of its 13°20' span). Some traditions assign
 *   direction by Vimshottari lord instead.
 *
 * @source Brihat Parashara Hora Shastra (Santhanam tr.); Brihat Samhita
 *         (Varahamihira); Muhurta Chintamani; Taittiriya Brahmana (for
 *         shakti triads). Tamil names from traditional Tamil Panchangam.
 */

